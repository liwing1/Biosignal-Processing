
function h = daub(n)
% h = daub(n)
% Daubechies Filters
% Function to supply Daubechies filter coefficients of filters up to 10th order
%  Inputs      n is filter order (can be 4,6,8,or 10)
%  Outputs     h is output coefficients
%
if n == 4 
    h = [.4829629131445341; .8365163037378077; .2241438680420134;...
            -.1294095225512603];
elseif n == 6
    h = [.3326705529500825; .8068915093110924; .4598775021184914;...
            -.1350110200102546; -.0854412738820267;.0352262918857095];
elseif n == 8
    h = [.2303778133088964; .7148465705529154; .6308807679398587;...
            -.0279837694168599; -.1870348117190931; .0308413818355607;...
            .0328830116668852; -.0105974017850690];
elseif n == 10
    h = [.1601023979741929; .6038292697971895; .72430855284377726;...
            .1384281459013203; -.2422948870663823; -.0322448695846381;...
            .0775714930400459; -.0062414902127893; -.0125807519990820;...
            .0033357252854738];
else
    h= 0;
    disp('Error in Daubechies filter order. n = ');
    disp(n);
end    
        